package Module1.basicjava;

public class stringfunctions {

	public static void main(String[] args) {
		String s="let me test ";
		 System.out.println(s.length());
		 System.out.println(s.indexOf("me"));
		 System.out.println(s.toUpperCase());
		 System.out.println(s.concat(" automation"));
		 System.out.println(s.contains("me"));
		 System.out.println(s.equals("me"));
		 System.out.println(s.equalsIgnoreCase("Let"));
		 System.out.println(s.charAt(4));
		 System.out.println(s.substring(4));
		 System.out.println(s.substring(7, 11));
		 System.out.println(s.replace("l", "L"));
		 System.out.println(s.trim());
		 
	}
 
}
